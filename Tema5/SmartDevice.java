package Tema5;

public class SmartDevice {

    String marca;
    String color;
    double precio;
    String modelo;
    int age;

    public SmartDevice() {

    }

    public SmartDevice(String marca, String color, double precio, String modelo, int age) {
        this.marca = marca;
        this.color = color;
        this.precio = precio;
        this.modelo = modelo;
        this.age = age;
    }


}
